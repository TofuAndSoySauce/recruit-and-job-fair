package kr.happyjob.study.resume.model;

public class ProposalResponse {

	public static class Proposal {

		private int proposalNo;
		private String memo;
		private String proposedDate;
		private String companyName;
		private String updatedAt;
		private int accepted;

		private int resumeNo;
		private String resumeTitle;
		private String experience;
		private String position;

		private int newProposalCount;
		private int totalProposalCount;

		public Proposal(int proposalNo, String memo, String proposedDate, String companyName, String updatedAt,
				int accepted, int resumeNo, String resumeTitle, String experience, String position,
				int newProposalCount, int totalProposalCount) {
			super();
			this.proposalNo = proposalNo;
			this.memo = memo;
			this.proposedDate = proposedDate;
			this.companyName = companyName;
			this.updatedAt = updatedAt;
			this.accepted = accepted;
			this.resumeNo = resumeNo;
			this.resumeTitle = resumeTitle;
			this.experience = experience;
			this.position = position;
			this.newProposalCount = newProposalCount;
			this.totalProposalCount = totalProposalCount;
		}

		public Proposal() {
			super();
		}

		public int getProposalNo() {
			return proposalNo;
		}

		public void setProposalNo(int proposalNo) {
			this.proposalNo = proposalNo;
		}

		public String getMemo() {
			return memo;
		}

		public void setMemo(String memo) {
			this.memo = memo;
		}

		public String getProposedDate() {
			return proposedDate;
		}

		public void setProposedDate(String proposedDate) {
			this.proposedDate = proposedDate;
		}

		public String getCompanyName() {
			return companyName;
		}

		public void setCompanyName(String companyName) {
			this.companyName = companyName;
		}

		public String getUpdatedAt() {
			return updatedAt;
		}

		public void setUpdatedAt(String updatedAt) {
			this.updatedAt = updatedAt;
		}

		public int getAccepted() {
			return accepted;
		}

		public void setAccepted(int accepted) {
			this.accepted = accepted;
		}

		public int getResumeNo() {
			return resumeNo;
		}

		public void setResumeNo(int resumeNo) {
			this.resumeNo = resumeNo;
		}

		public String getResumeTitle() {
			return resumeTitle;
		}

		public void setResumeTitle(String resumeTitle) {
			this.resumeTitle = resumeTitle;
		}

		public String getExperience() {
			return experience;
		}

		public void setExperience(String experience) {
			this.experience = experience;
		}

		public String getPosition() {
			return position;
		}

		public void setPosition(String position) {
			this.position = position;
		}

		public int getNewProposalCount() {
			return newProposalCount;
		}

		public void setNewProposalCount(int newProposalCount) {
			this.newProposalCount = newProposalCount;
		}

		public int getTotalProposalCount() {
			return totalProposalCount;
		}

		public void setTotalProposalCount(int totalProposalCount) {
			this.totalProposalCount = totalProposalCount;
		}

	}

	public static class ProposedCompany {

		private String companyName;
		private String email;
		private String phone;
		private String establishedDate;
		private String address;
		private String imageURL;
		private String category;
		private String size;

		public ProposedCompany(String companyName, String email, String phone, String establishedDate, String address,
				String imageURL, String category, String size) {
			super();
			this.companyName = companyName;
			this.email = email;
			this.phone = phone;
			this.establishedDate = establishedDate;
			this.address = address;
			this.imageURL = imageURL;
			this.category = category;
			this.size = size;
		}

		public String getImageURL() {
			return imageURL;
		}

		public void setImageURL(String imageURL) {
			this.imageURL = imageURL;
		}

		public String getAddress() {
			return address;
		}

		public void setAddress(String address) {
			this.address = address;
		}

		public String getEstablishedDate() {
			return establishedDate;
		}

		public void setEstablishedDate(String establishedDate) {
			this.establishedDate = establishedDate;
		}


		public String getCompanyName() {
			return companyName;
		}

		public void setCompanyName(String companyName) {
			this.companyName = companyName;
		}

		public String getEmail() {
			return email;
		}

		public void setEmail(String email) {
			this.email = email;
		}

		public String getPhone() {
			return phone;
		}

		public void setPhone(String phone) {
			this.phone = phone;
		}

		public String getCategory() {
			return category;
		}

		public void setCategory(String category) {
			this.category = category;
		}

		public String getSize() {
			return size;
		}

		public void setSize(String size) {
			this.size = size;
		}

		public ProposedCompany() {
			super();
		}

	}

}
