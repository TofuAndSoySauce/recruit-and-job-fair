package kr.happyjob.study.resume.model;

public class ResumeSubmitModel {
	
	private int submit_no;//이력서번호
	private String submit_memo;//
	private String submit_status;
	private String read_yn;
	private String interview_yn;
	private String reg_date;
	private int ad_no;
	private int resume_no;
	private String submit_interviewdate;//면접일자
	private String submit_interviewtime;//면접시간
	
	
	public int getSubmit_no() {
		return submit_no;
	}
	public void setSubmit_no(int submit_no) {
		this.submit_no = submit_no;
	}
	public String getSubmit_memo() {
		return submit_memo;
	}
	public void setSubmit_memo(String submit_memo) {
		this.submit_memo = submit_memo;
	}
	public String getSubmit_status() {
		return submit_status;
	}
	public void setSubmit_status(String submit_status) {
		this.submit_status = submit_status;
	}
	public String getRead_yn() {
		return read_yn;
	}
	public void setRead_yn(String read_yn) {
		this.read_yn = read_yn;
	}
	public String getInterview_yn() {
		return interview_yn;
	}
	public void setInterview_yn(String interview_yn) {
		this.interview_yn = interview_yn;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public int getAd_no() {
		return ad_no;
	}
	public void setAd_no(int ad_no) {
		this.ad_no = ad_no;
	}
	public int getResume_no() {
		return resume_no;
	}
	public void setResume_no(int resume_no) {
		this.resume_no = resume_no;
	}
	public String getSubmit_interviewdate() {
		return submit_interviewdate;
	}
	public void setSubmit_interviewdate(String submit_interviewdate) {
		this.submit_interviewdate = submit_interviewdate;
	}
	public String getSubmit_interviewtime() {
		return submit_interviewtime;
	}
	public void setSubmit_interviewtime(String submit_interviewtime) {
		this.submit_interviewtime = submit_interviewtime;
	}
	@Override
	public String toString() {
		return "ResumeSubmitModel [submit_no=" + submit_no + ", submit_memo=" + submit_memo + ", submit_status="
				+ submit_status + ", read_yn=" + read_yn + ", interview_yn=" + interview_yn + ", reg_date=" + reg_date
				+ ", ad_no=" + ad_no + ", resume_no=" + resume_no + ", submit_interviewdate=" + submit_interviewdate
				+ ", submit_interviewtime=" + submit_interviewtime + "]";
	}
	
	
	

	
	
}
