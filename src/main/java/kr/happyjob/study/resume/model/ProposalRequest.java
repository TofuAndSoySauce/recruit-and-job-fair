package kr.happyjob.study.resume.model;

public class ProposalRequest {

	public static class Patch {

		private boolean accepted;
		private int proposalNo;
		private String proposalMemo;
		private String loginId;
		private String redirectPageUrl;

		public String getRedirectPageUrl() {
			return redirectPageUrl;
		}

		public void setRedirectPageUrl(String redirectPageUrl) {
			this.redirectPageUrl = redirectPageUrl;
		}

		public Patch(boolean accepted, int proposalNo, String proposalMemo, String loginId, String redirectPageUrl) {
			super();
			this.accepted = accepted;
			this.proposalNo = proposalNo;
			this.proposalMemo = proposalMemo;
			this.loginId = loginId;
			this.redirectPageUrl = redirectPageUrl;
		}

		public boolean isAccepted() {
			return accepted;
		}

		public void setAccepted(boolean accepted) {
			this.accepted = accepted;
		}

		public int getProposalNo() {
			return proposalNo;
		}

		public void setProposalNo(int proposalNo) {
			this.proposalNo = proposalNo;
		}

		public String getProposalMemo() {
			return proposalMemo;
		}

		public void setProposalMemo(String proposalMemo) {
			this.proposalMemo = proposalMemo;
		}

		public String getLoginId() {
			return loginId;
		}

		public void setLoginId(String loginId) {
			this.loginId = loginId;
		}

		public Patch() {
			super();
		}

	}
}
