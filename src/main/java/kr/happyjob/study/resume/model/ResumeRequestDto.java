package kr.happyjob.study.resume.model;

public class ResumeRequestDto {

	public static class Post {
		
		private String loginId;
		private int adNo;
		private int resumeNo;
		private int submitNo;
		private String redirectPageUrl;
		
		public Post(String loginId, int adNo, int resumeNo, int submitNo, String redirectPageUrl) {
			super();
			this.loginId = loginId;
			this.adNo = adNo;
			this.resumeNo = resumeNo;
			this.submitNo = submitNo;
			this.redirectPageUrl = redirectPageUrl;
		}
		
		public Post() {
			super();
		}

		public String getLoginId() {
			return loginId;
		}

		public void setLoginId(String loginId) {
			this.loginId = loginId;
		}

		public int getAdNo() {
			return adNo;
		}

		public void setAdNo(int adNo) {
			this.adNo = adNo;
		}

		public int getResumeNo() {
			return resumeNo;
		}

		public void setResumeNo(int resumeNo) {
			this.resumeNo = resumeNo;
		}

		public int getSubmitNo() {
			return submitNo;
		}

		public void setSubmitNo(int submitNo) {
			this.submitNo = submitNo;
		}

		public String getRedirectPageUrl() {
			return redirectPageUrl;
		}

		public void setRedirectPageUrl(String redirectPageUrl) {
			this.redirectPageUrl = redirectPageUrl;
		}
	
		
	}
	
}
