package kr.happyjob.study.community.model;



public class QnaModel {

	private int qna_no;
	private String qna_title;
	private String qna_content;
	private int qna_answer;
	private String reg_date;
	private String update_date;
	private int is_delete;
	private String loginID;
	private int file_cd;
	
	
	
	public int getQna_no() {
		return qna_no;
	}
	public void setQna_no(int qna_no) {
		this.qna_no = qna_no;
	}
	public String getQna_title() {
		return qna_title;
	}
	public void setQna_title(String qna_title) {
		this.qna_title = qna_title;
	}
	public String getQna_content() {
		return qna_content;
	}
	public void setQna_content(String qna_content) {
		this.qna_content = qna_content;
	}
	public int getQna_answer() {
		return qna_answer;
	}
	public void setQna_answer(int qna_answer) {
		this.qna_answer = qna_answer;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
	public int getIs_delete() {
		return is_delete;
	}
	public void setIs_delete(int is_delete) {
		this.is_delete = is_delete;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public int getFile_cd() {
		return file_cd;
	}
	public void setFile_cd(int file_cd) {
		this.file_cd = file_cd;
	}
	@Override
	public String toString() {
		return "QnaModel [qna_no=" + qna_no + ", qna_title=" + qna_title + ", qna_content=" + qna_content
				+ ", qna_answer=" + qna_answer + ", reg_date=" + reg_date + ", update_date=" + update_date
				+ ", is_delete=" + is_delete + ", loginID=" + loginID + ", file_cd=" + file_cd + "]";
	}

	
	
	
	
	
	
	
	
}
